package pl.zadanie_2;

public class MesureUnits {

double meterToCentimeter(double meter){
    return meter*100;
}

double meterToMilimeter(double meter){
    return meter*1000;
}

double kilogramToGram(double kilogram){
    return kilogram*1000;
}

double kilogramToMiligram(double kilogram){
    return kilogram*1000000;
}

}
